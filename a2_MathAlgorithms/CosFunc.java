import java.util.*;
/**
*Class that creates the evaluate function for Cos
*
*
*
*/
public class CosFunc extends Function 
{
	
	/**
	* Method that evaluates the cos for the value given
	*
	*@param x given variable to be evaluated
	*@return Math.cos(x) 
	*/
	public double evaluate(double x)
	{
		return Math.cos(x);
	}
}